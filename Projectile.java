public class Projectile
{
    int yLocation;
    int xLocation;
    public void setYLocation(int yLoc)
    {
        yLocation = yLoc;
    }

    public void setXLocation(int xLoc)
    {
        xLocation = xLoc;
    }

    public int getYLocation()
    {
        return yLocation;
    }

    public int getXLocation()
    {
        return xLocation;
    }  
}